package myPackage;

public class Datatypes {

	public static void main(String[] args) {
		
		//Premitive Data types 
		int a=10;
		double d=10.5;
		char c='H';
		boolean b= false;
		
		//Drived data types
		String s="Welcome";
		
		System.out.println(a);
		System.out.println(d);
		System.out.println(c);
		System.out.println(b);
		System.out.println(s);
		
	}

}
